package unified

import (
	"context"

	"github.com/UnipayFI/go-gate/v4/client"
	"github.com/UnipayFI/go-gate/v4/request"
)

var _ request.Client = (*UnifiedClient)(nil)

// UnifiedClient is the REST client for Gate's unified-account endpoints under /api/v4/unified/*.
type UnifiedClient struct {
	*client.Client
}

// NewUnifiedClient constructs a unified-account REST client.
func NewUnifiedClient(options ...client.Options) *UnifiedClient {
	return &UnifiedClient{client.NewClient(options...)}
}

// SyncServerTime measures the client/server clock offset (via GET /api/v4/spot/time,
// Gate's single server-time source) and stores it so signed requests carry a
// Timestamp Gate accepts.
func (c *UnifiedClient) SyncServerTime(ctx context.Context) error {
	offset, server, err := request.FetchServerTimeOffset(ctx, c)
	if err != nil {
		return err
	}
	c.SetTimeOffset(offset)
	c.GetLogger().Infof("Time sync: server=%d, offset=%dms", server, c.GetTimeOffsetMs())
	return nil
}
